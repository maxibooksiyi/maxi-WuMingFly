/***************************主要核心：传感器滤波、姿态解算、惯导、控制等代码在TIME.c里面运行**********************************/
int main()
{
  NCQ_Init();//硬件资源+相关外设初始化
  delay_ms(500);
  while(1)
  {
    Key_Scan(Key_Right_Release);							//按键扫描
    QuadShow();																//显示屏刷新
    Save_Or_Reset_PID_Parameter();						//运用地面站，修改控制参数
    Accel_Calibartion();											//加速度计标定
    Mag_Calibartion_LS(DataMag,Circle_Angle);	//最小二乘法磁力计球面拟合
    RC_Calibration_Check(PPM_Databuf);				//遥控器行程标定
  }
}
